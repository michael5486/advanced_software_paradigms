    var int: Int = 2
    var long: Long = 3
    var float: Float = 2.50f
    var double: Double = 2.50
    var byte: Byte = 0xa
    var char: Char = 'q'
    var string: String = "i am a string"
    
    println(bool)
    println(short)
    println(int)
    println(long)
    println(float)
    println(double)
    println(byte)
    println(char)
    println(string)
    
  }
}